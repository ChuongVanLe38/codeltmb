void main(){
    //Tăng/ giảm
  //++ --
  int y =100;
  y++; //y = y+1;
  y--; //y = y-1;

  int a = 5;
  print(a++);//in 5, a=6(in trước rồi thực hiện)
  print(++a);//in 7, a=7(thực hiện trước rồi in)
  print(a--);//in 7, a=6(in trước rồi thực hiện)
  print(--a);//in 5, a=5(thực hiện trước rồi in)
  print(a);// a=5
}